#  in python we have 3 types of `numbers`:
# 1. int (integer) - whole numbers
# 2. float (floating point) - decimal numbers
# 3. complex - numbers with real and imaginary parts

x = 1
y = 1.1
x = 1 + 2j

# we also have mathematical operations:

# addition
print(2 + 2)
# subtraction
print(2 - 2)
# multiplication
print(2 * 2)
# division
print(10 / 3)  # floor division
print(10 // 3)  # normal division

# exponentiation
print(2 ** 3)  # 2 to the power of 3


# WE ALSO HAVE AUGMENTED ASSIGNMENT OPERATORS

x = 10
# we have to increment x by 3

x += 3  # this is equivalent to x = x + 3

print(x)  # this will print 13

# Functions with Numbers

# Round
print(round(2.9))

# Absolute Value
print(abs(-2.9))
# Maximum and Minimum
print(max(2, 3, 4))
print(min(2, 3, 4))

# Power
print(pow(2, 3))  # 2 to the power of 3
# Sum (with list)
print(sum([1, 2, 3, 4, 5]))  # sum of the list
